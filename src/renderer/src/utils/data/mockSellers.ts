/* FUNCTIONS */
//Calculate the profit and the comission of the operations.
function enrichOperations(
  operations: Omit<OperationsExample, "netProfit" | "commission">[],
): OperationsExample[] {
  const enriched: OperationsExample[] = [];

  for (let i = 0; i < operations.length; i++) {
    const op = operations[i];

    const amount = op.amount;
    const price = op.price;
    const marketPrice = op.marketPrice;

    const total = amount * price;
    const marketTotal = amount * marketPrice;

    let grossProfit;

    if (op.operationType === "Venta") {
      grossProfit = total - marketTotal;
    } else {
      grossProfit = marketTotal - total;
    }

    const commission = grossProfit / 2;
    const netProfit = grossProfit - commission;

    enriched.push({
      ...op,
      commission,
      netProfit,
    });
  }

  return enriched;
}

